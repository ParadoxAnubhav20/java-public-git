public class DataTypes {
    public static void main(String[] args) {
        int val = 5;
        float f = 3.5f;
        char ch = 'A';
        double data = 87.6543;
        short s = 12;
        long l = 1234567890L;
        byte b = 127;
        boolean bool = true;
        
        System.out.println("Value of Integer Variable: " + val);
        System.out.println("Value of Float Variable: " + f);
        System.out.println("Value of Char Variable: " + ch);
        System.out.println("Value of Double Variable: " + data);
        System.out.println("Value of Short Variable: " + s);
        System.out.println("Value of Long Variable: " + l);
        System.out.println("Value of Byte Variable: " + b);
        System.out.println("Value of Boolean Variable: " + bool);
    }
}
